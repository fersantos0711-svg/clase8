// operadores aritmeticos

let a = 10;
let b = 5;

console.log (a + b); // suma
console.log (a - b); // resta
console.log (a * b); // multiplicacion
console.log (a / b); // division
console.log (a % b); // residuo

//operadores de comparacion

console.log (a > b); // mayor que true
console.log (a < b); // menor que false
console.log (a >= b); // mayor o igual que false
console.log (a <= b); // menor o igual false
console.log (a === "10"); //estrictamente igual true

//operadores logicos
console.log (true && false); // me devuelve solo true si mambos solo si ambos son true

console.log (true || false); // me devuelve true si al menos uno es true